function myFunction1() {
    var x = document.getElementById("dcard1");
    var submit = document.getElementById("Read1");
    var ask = document.getElementById("ask1");
    if (x.style.height === "206px" || submit.value === 'Read more') {
        x.style.height="336px";x.style.border="1px solid black";
        submit.value = 'Read less';submit.style.top="294px";
        ask.style.top="294px";
    } else {
        x.style.height = "206px";
        submit.value = 'Read more';submit.style.top="160px";
        ask.style.top="160px";
    }
}
